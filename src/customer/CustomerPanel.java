    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        try
        {
            ResultSet rs  = DBOperation.getCustomerShoppingDetails(rm.getPhone_number());
            while(rs.next())
            {
                String items = rs.getString("items");
                
                String[] items_arr = items.split(",");
                
                int items_arr_length = items_arr.length;
                
                int number_of_items = items_arr_length/4;
                
                int index_position = -1;
                
                String items_list = "";
                
                for(int i=1;i<=number_of_items;i++)
                {
                    index_position = index_position + 2;
                    String item_name = items_arr[index_position];
                    
                    index_position = index_position + 1;
                    String item_price = items_arr[index_position];
                    
                    index_position = index_position + 1;
                    String item_quantity = items_arr[index_position];
                    
                    items_list = item_quantity+" "+item_name+" of each "+item_price+"rs || "+items_list;
                }
                
                String date = rs.getString("datee");
                String time = rs.getString("timee");
                String email = rs.getString("email");
                
                
                Object[] obj = {items_list,date,time,email};
                dtm.addRow(obj);
//                Object[] obj={items_list, datee, timee, emp_email};
//                dtm.addRow(obj);
                
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_formWindowOpened
    
    
    
    
